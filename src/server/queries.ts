// ----- English-baseline frequencies (wordfreq, generated by scripts/build-baseline.py) -----
//
// Loaded once at module import. The baseline is a Zipf score per word (log10 per-billion;
// 0 = unknown). We convert to a per-token rate at use-time: pow(10, zipf - 9) gives
// frequency-per-token (since 10^9 tokens ≈ 1 billion). This lets us compute:
//
//   corpus_rate(w) / baseline_rate(w) = "lift" — how much more we use w than average.
//
// Generated by `.venv-baseline/bin/python3 scripts/build-baseline.py`. If the file is
// missing (e.g. fresh checkout), we fall back to an empty map: every lookup returns
// the unknown-word default (zipf=1.0 ≈ "extremely rare in English"), which gives all
// our words a uniformly-large lift and iconic_score collapses to a constant. The UI
// guards against that case but you should regenerate.
type BaselineFile = {
  _meta?: Record<string, unknown>;
  frequencies?: Record<string, number>;
};

const BASELINE_PATH = projectPath("data/baseline-frequencies.json");
const BASELINE: Record<string, number> = (() => {
  try {
    const raw = readTextFile(BASELINE_PATH);
    const parsed = JSON.parse(raw) as BaselineFile;
    const freqs = parsed.frequencies ?? {};
    const meta = parsed._meta ?? {};
    console.log(
      `[baseline] loaded ${Object.keys(freqs).length} words from ${BASELINE_PATH}` +
        (meta.coverage_pct != null ? ` (coverage ${meta.coverage_pct}%)` : ""),
    );
    return freqs;
  } catch (err) {
    console.warn(
      `[baseline] could not load ${BASELINE_PATH}: ${(err as Error).message}. ` +
        `Lift/iconic scoring will be degraded. Run 'scripts/build-baseline.py'.`,
    );
    return {};
  }
})();

// Zipf score for unknown words. 1.0 ≈ ~1 in 10^8 tokens — moderately rare.
// Lower = more lift for unknowns (good for in-jokes/names) but also lifts pure typos.
const UNKNOWN_ZIPF = 1.0;

function zipfToRate(zipf: number): number {
  // Zipf = log10(per-billion). per-token = 10^(zipf - 9).
  return Math.pow(10, zipf - 9);
}

function baselineZipfFor(word: string): number {
  const z = BASELINE[word];
  // Treat 0 (= "not in wordfreq large list") as unknown.
  if (z == null || z === 0) return UNKNOWN_ZIPF;
  return z;
}

