function homeScopeCacheKey(name: string, scope: MessageScope): string {
  return `home:${name}:${JSON.stringify({
    from: scope.from ?? "",
    phase: scope.phase ?? "",
    sender: scope.sender ?? "both",
    to: scope.to ?? "",
  })}`;
}

// ----- stats -----
export const getStats = createServerFn({ method: "GET" })
  .inputValidator((d: unknown) => messageScopeInput.parse(d ?? {}))
  .handler(async ({ data }): Promise<Stats> => {
  const resolved = resolveMessageScope(data);
  return withDbCache(homeScopeCacheKey("stats", resolved), () => {
  const d = db();
  const scope = messageScopeWhere(resolved);

  const base = d.prepare(`
    SELECT
      COUNT(*) AS total,
      SUM(is_from_me) AS me,
      SUM(1-is_from_me) AS them,
      MIN(ts) AS first_ts,
      MAX(ts) AS last_ts,
      SUM(has_attachment) AS attachments,
      SUM(CASE WHEN associated_message_type BETWEEN 2000 AND 2005 THEN 1 ELSE 0 END) AS tapbacks,
      SUM(CASE WHEN reply_to_guid IS NOT NULL THEN 1 ELSE 0 END) AS replies,
      SUM(word_count) AS total_words,
      SUM(char_count) AS total_chars
    FROM messages m
    ${scope.sql}
  `).get(...scope.args) as Record<string, number>;

  const dayCounts = new Map<string, number>();
  const dayRows = d.prepare(`SELECT m.ts FROM messages m ${scope.sql}`).iterate(...scope.args) as IterableIterator<{ ts: number }>;
  for (const row of dayRows) {
    const ymd = bucket(row.ts, "ymd");
    dayCounts.set(ymd, (dayCounts.get(ymd) ?? 0) + 1);
  }
  const busy = [...dayCounts.entries()].sort((a, b) => b[1] - a[1] || a[0].localeCompare(b[0]))[0];

  // longest gap between consecutive messages (real-text only — exclude tapbacks)
  const realScope = messageScopeWhere(resolved, "m", [realMessageWhere("text_turn", "m")]);
  const gap = d.prepare(`
    WITH ordered AS (
      SELECT ts, LAG(ts) OVER (ORDER BY ts) AS prev_ts
      FROM messages m
      ${realScope.sql}
    )
    SELECT prev_ts AS gap_start, ts AS gap_end, (ts - prev_ts) AS gap_sec
    FROM ordered
    WHERE prev_ts IS NOT NULL
    ORDER BY gap_sec DESC
    LIMIT 1
  `).get(...realScope.args) as { gap_start: number; gap_end: number; gap_sec: number } | undefined;

  // median gap
  const medGap = d.prepare(`
    WITH ordered AS (
      SELECT ts, LAG(ts) OVER (ORDER BY ts) AS prev_ts
      FROM messages m
      ${realScope.sql}
    ),
    diffs AS (SELECT (ts - prev_ts) AS gap FROM ordered WHERE prev_ts IS NOT NULL)
    SELECT gap FROM diffs ORDER BY gap LIMIT 1 OFFSET (SELECT COUNT(*)/2 FROM diffs)
  `).get(...realScope.args) as { gap: number } | undefined;

  // median reply latency by direction (real reply = previous msg from other party)
  const medReply = d.prepare(`
    WITH ordered AS (
      SELECT ts, is_from_me,
             LAG(ts) OVER (ORDER BY ts) AS prev_ts,
             LAG(is_from_me) OVER (ORDER BY ts) AS prev_from_me
      FROM messages m
      ${realScope.sql}
    ),
    me_replies AS (
      SELECT (ts - prev_ts) AS lat FROM ordered
      WHERE prev_ts IS NOT NULL AND is_from_me=1 AND prev_from_me=0 AND (ts - prev_ts) < 86400
    ),
    them_replies AS (
      SELECT (ts - prev_ts) AS lat FROM ordered
      WHERE prev_ts IS NOT NULL AND is_from_me=0 AND prev_from_me=1 AND (ts - prev_ts) < 86400
    )
    SELECT
      (SELECT lat FROM me_replies ORDER BY lat LIMIT 1 OFFSET (SELECT COUNT(*)/2 FROM me_replies)) AS me_to_them,
      (SELECT lat FROM them_replies ORDER BY lat LIMIT 1 OFFSET (SELECT COUNT(*)/2 FROM them_replies)) AS them_to_me
  `).get(...realScope.args) as { me_to_them: number; them_to_me: number } | undefined;

  return {
    total: base.total ?? 0,
    me: base.me ?? 0,
    them: base.them ?? 0,
    first_ts: base.first_ts ?? 0,
    last_ts: base.last_ts ?? 0,
    attachments: base.attachments ?? 0,
    tapbacks: base.tapbacks ?? 0,
    replies: base.replies ?? 0,
    total_words: base.total_words ?? 0,
    total_chars: base.total_chars ?? 0,
    days_with_messages: dayCounts.size,
    longest_gap_days: gap ? Math.round(gap.gap_sec / 86400) : 0,
    longest_gap_start: gap?.gap_start ?? 0,
    longest_gap_end: gap?.gap_end ?? 0,
    busiest_day_ymd: busy?.[0] ?? "",
    busiest_day_count: busy?.[1] ?? 0,
    median_gap_seconds: medGap?.gap ?? 0,
    reply_med_me_to_them: medReply?.me_to_them ?? 0,
    reply_med_them_to_me: medReply?.them_to_me ?? 0,
    generated_at: getDataGeneratedAt(),
  };
  });
});

// ----- monthly counts -----
export const getMonthly = createServerFn({ method: "GET" })
  .inputValidator((d: unknown) => messageScopeInput.parse(d ?? {}))
  .handler(async ({ data }) => {
    const resolved = resolveMessageScope(data);
    return withDbCache(homeScopeCacheKey("monthly", resolved), () => {
    const scope = messageScopeWhere(resolved);
    const rows = db().prepare(`
      SELECT m.ts, m.is_from_me
      FROM messages m
      ${scope.sql}
      ORDER BY m.ts
    `).all(...scope.args) as Array<{ ts: number; is_from_me: number }>;
    const byMonth = new Map<string, { ym: string; me: number; them: number; total: number }>();
    for (const row of rows) {
      const ym = bucket(row.ts, "ym");
      const month = byMonth.get(ym) ?? { ym, me: 0, them: 0, total: 0 };
      month.total += 1;
      if (row.is_from_me === 1) month.me += 1;
      else month.them += 1;
      byMonth.set(ym, month);
    }
    return [...byMonth.values()].sort((a, b) => a.ym.localeCompare(b.ym));
    });
  });

