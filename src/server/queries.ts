const GAME_GRID = new Set([
  "⬛","⬜","🟧","🟨","🟩","🟦","🟪","🟥","🟫",
  "⚫","⚪","🔴","🟠","🟡","🟢","🔵","🟣","🟤",
  "⭐","✅","❌","➡️","⬅️","⬆️","⬇️",
]);
export const getEmojiTop = createServerFn({ method: "GET" })
  .inputValidator((d: unknown) => messageScopeInput.parse(d ?? {}))
  .handler(async ({ data }) => {
    const resolved = resolveMessageScope(data);
    return withDbCache(homeScopeCacheKey("emoji-top", resolved), () => {
    const scope = messageScopeWhere(resolved, "m", ["m.text IS NOT NULL", "m.text != ''"]);
    const rows = db().prepare(`
      SELECT m.text FROM messages m ${scope.sql}
    `).iterate(...scope.args) as IterableIterator<{ text: string }>;
    const counts = new Map<string, number>();
    const emoRe = /(?:\p{Extended_Pictographic}(?:‍\p{Extended_Pictographic})*[️]?)/gu;
    for (const r of rows) {
      const m = r.text.match(emoRe);
      if (!m) continue;
      for (const e of m) {
        if (GAME_GRID.has(e)) continue;
        counts.set(e, (counts.get(e) ?? 0) + 1);
      }
    }
    const out = [...counts.entries()].sort((a, b) => b[1] - a[1]).slice(0, 30).map(([emoji, n]) => ({ emoji, n }));
    return out;
    });
  });

